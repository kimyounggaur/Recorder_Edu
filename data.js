/* 음이 어느 손을 쓰는지 계산 */
function noteHands(note, system) {
  const f = (system === 'G' && note.fG) ? note.fG : note.f;
  let left = f.t > 0, right = false;
  f.h.forEach((v, i) => { if (v > 0) { if (HOLE_HAND[i] === 'left') left = true; else right = true; } });
  return left && right ? 'both' : (right ? 'right' : 'left');
}
